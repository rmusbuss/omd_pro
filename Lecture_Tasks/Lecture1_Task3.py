from abc import abstractmethod, ABC


class PokemonTrainInterface(ABC):
    """
    –ê–±—Å—Ç—Ä–∞–∫—Ç–Ω—ã–π –∫–ª–∞—Å—Å —Ç—Ä–µ–Ω–∏—Ä–æ–≤–æ–∫ –ø–æ–∫–µ–º–æ–Ω–æ–≤, –∫–æ—Ç–æ—Ä—ã–π —Å—Å–æ–¥–µ—Ä–∂–∏—Ç
    –æ–¥–∏–Ω –∞–±—Å—Ç—Ä–∞–∫—Ç–Ω—ã–π –º–µ—Ç–æ–¥ increase_experience, –∏ —Å–≤–æ–π—Å—Ç–≤–æ experience
    –∫–æ—Ç–æ—Ä—ã–π –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã –∫ —Ä–µ–∞–ª–∏–∑–∞—Ü–∏–∏ –≤ –∫–ª–∞—Å—Å–∞—Ö –Ω–∞—Å–ª–µ–¥–Ω–∏–∫–∞—Ö
    """

    @abstractmethod
    def increase_experience(self, value: int) -> None:
        pass

    @property
    @abstractmethod
    def experience(self) -> int:
        pass


class BasePokemon(PokemonTrainInterface):
    """
    –ö–ª–∞—Å—Å, –∫–æ—Ç–æ—Ä—ã–π —Å–æ–∑–¥–∞–µ—Ç –ø–æ–∫–µ–º–æ–Ω–∞ —Å –∞—Ç—Ä–∏–±—É—Ç–∞–º–∏ –∏–º—è –∏ —Ç–∏–ø –ø–æ–∫–µ–º–æ–Ω–∞
    –ê —Ç–∞–∫–∂–µ –ø–µ—Ä–µ–æ–ø—Ä–µ–¥–µ–ª—è–µ—Ç –º–µ—Ç–æ–¥ —Å—Ç—Ä–æ–∫–æ–≤–æ–≥–æ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—è –æ–±—ä–µ–∫—Ç–∞
    –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ –Ω–∞—Å–ª–µ–¥—É–µ—Ç—Å—è –æ—Ç –∞–±—Å—Ç—Ä–∞–∫—Ç–Ω–æ–≥–æ –∫–ª–∞—Å—Å–∞ –∏ —Ä–µ–∞–ª–∏–∑—É–µ—Ç –æ–¥–∏–Ω
    –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–π –º–µ—Ç–æ–¥ –∏ –æ–¥–Ω–æ —Å–≤–æ–π—Å—Ç–≤–æ increase_experience –∏ experience
    """

    def __init__(self, name: str, poketype: str):
        """
        –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä, –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É—é—â–∏–π –∏–º—è –∏ —Ç–∏–ø –ø–æ–∫–µ–º–æ–Ω–∞, –∞ —Ç–∞–∫–∂–µ
        –Ω–∞—á–∞–ª—å–Ω—ã–π –æ–ø—ã—Ç
        """
        self.name = name
        self.poketype = poketype
        self.exp = 100

    def increase_experience(self, value: int) -> None:
        """
        –º–µ—Ç–æ–¥, –∫–æ—Ç–æ—Ä—ã–π –ø–æ–ª—É—á–∞–µ—Ç –Ω–∞ –≤—Ö–æ–¥ –Ω–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –æ–ø—ã—Ç–∞ –¥–ª—è –ø–æ–∫–µ–º–æ–Ω–∞
        –∏ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ—Ç –µ–≥–æ
        """
        self.exp += 100

    @property
    def experience(self) -> int:
        """
        —Å–≤–æ–π—Å—Ç–≤–æ, –∫–æ—Ç–æ—Ä–æ–µ –≤—ã–≤–æ–¥–∏—Ç –∑–Ω–∞—á–µ–Ω–∏–µ –æ–ø—ã—Ç–∞ –ø–æ–∫–µ–º–æ–Ω–∞
        """
        return self.exp

    def __str__(self) -> str:
        """
        –º–∞–≥–∏—á–µ—Å–∫–∏–π –º–µ—Ç–æ–¥ –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Å—Ç—Ä–æ–∫–æ–≤–æ–≥–æ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—è –æ–±—ä–µ–∫—Ç–∞
        """
        return f'{self.name}/{self.poketype}'


